    private void loadCustomerID() throws ClassNotFoundException, SQLException{
        ArrayList<String> allCustomers = CustomerController.loadCustomerID();
        for (String item : allCustomers) {
            cmdCustomerID.addItem(item);
        }
        
    }
    
    public void loadReservation() throws ClassNotFoundException, SQLException{
        ArrayList<Reservation> AllReservation = ReservationController.loadReservation();
        DefaultTableModel dtm = (DefaultTableModel) tblReservation.getModel();
        dtm.setRowCount(0);
        for (Reservation reservation : AllReservation) {
            Object[] rowdata = {
                reservation.getRid(),
                reservation.getCustomer_id(),
                reservation.getReturn_date(),
                reservation.getPrice(),
                reservation.getReservation_date_time()
            };
            dtm.addRow(rowdata);
        }    
    }
    
    public void loadRID() throws ClassNotFoundException{
        try {
            ReservationController reservationController=new ReservationController();
            String id;
            id = reservationController.getRId();
            txtRID.setText(id);
            txtRID.setEditable(false);
        } catch (SQLException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
