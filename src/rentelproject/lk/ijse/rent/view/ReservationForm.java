    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String rid = txtRID.getText();
        String customer_id = cmdCustomerID.getSelectedItem().toString();
        double price = Double.parseDouble(txtPrice.getText());
        String reservation_date = txtDate.getText();
        String return_date = txtReturnDate.getText();
        Reservation reservation = new Reservation(rid, customer_id, return_date, reservation_date, price);
        try {
            boolean isAded = ReservationController.deleteReservation(reservation);
            if (isAded) {
                JOptionPane.showMessageDialog(this, "REMOVED");
                DefaultTableModel dtm = (DefaultTableModel) tblReservation.getModel();
                dtm.setRowCount(0);
                loadReservation();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }  
    }//GEN-LAST:event_btnRemoveActionPerformed

