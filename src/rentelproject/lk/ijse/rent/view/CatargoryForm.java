    private void btnCatargorySaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCatargorySaveActionPerformed
        String id = txtCatargoryID.getText();
        String name = txtName.getText();
        int qty = Integer.parseInt(txtQuantity.getText());
        Catargory catargory = new Catargory(id, name, qty); 
        try {
            boolean isAdded =CatargoryController.addCatargory(catargory);
            
            if (isAdded) {
                Object row[]={id,name,qty};
                DefaultTableModel dtm = (DefaultTableModel) tblCatargory.getModel();
                dtm.addRow(row);
                this.dispose();
                new CatargoryForm().setVisible(true);
                JOptionPane.showMessageDialog(this, "Added");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CatargoryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }   
    }//GEN-LAST:event_btnCatargorySaveActionPerformed

