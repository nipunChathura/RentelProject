    public void allCatargory() throws ClassNotFoundException, SQLException{
        ArrayList<Catargory> allCatargory = CatargoryController.loadCatargory();
        DefaultTableModel dtm = (DefaultTableModel) tblCatargory.getModel();
        dtm.setRowCount(0);
        for (Catargory catargory1 : allCatargory) {
            Object[] rowdata = {
                catargory1.getCatargory_id(),
                catargory1.getName(),
                catargory1.getQuantity()
            };
            dtm.addRow(rowdata);
        }  
    }
    
