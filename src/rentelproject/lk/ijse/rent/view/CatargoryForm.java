    private void btnCatargoryUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCatargoryUpdateActionPerformed
        Catargory catargory = new Catargory(txtCatargoryID.getText(), txtName.getText(), Integer.parseInt(txtQuantity.getText()));
        try {
            boolean isUpdate = CatargoryController.updateCatargory(catargory);
            if (isUpdate) {
                JOptionPane.showMessageDialog(this, "UPDATED");
                DefaultTableModel dtm = (DefaultTableModel) tblCatargory.getModel();
                dtm.setRowCount(0);
                allCatargory();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CatargoryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnCatargoryUpdateActionPerformed

